
// Atribuição e ternário


let numero = 20
let numero2 = 40

numero += numero2; // numero = numero + 10
console.log(numero);

numero -= numero2; // numero = numero - 10
console.log(numero);

numero *= numero2; // numero = numero * 10
console.log(numero);      

numero /= numero2; // numero = numero / 10
console.log(numero);

